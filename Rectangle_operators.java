package assignments;

public class Rectangle_operators {
    public static void main(String[] args) {
        int length,breadth;
        int area,perimeter;
        length=5;
        breadth=7;
        area=length*breadth;
        perimeter=2*(length+breadth);
        System.out.println("Area of a rectangle "+area);
        System.out.println("Perimeter of a rectangle "+perimeter);
    }
}
